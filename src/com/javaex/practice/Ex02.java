package com.javaex.practice;

public class Ex02 {

	public static void main(String[] args) {
		
		System.out.println( 12 / 5 - 3 );
		System.out.println( 5 + 19 % 3 );
		
		
		// System.out.println( 12 / 5 - 3 ); 예상
		// 12 / 5 첫번째, - 3이 두번째로 계산해야하기 때문에, 12 나누기 5 -3 = -1  -> /는 나누는 몫의 값 
		
		// System.out.println( 5 + 19 % 3 ); 예상
		// 19 % 3 첫번째, 5 +가 두번째로 계산하여, 19 나누기 3 + 5 = 6  -> %는 나누는 나머지의 값 
		

	}

}
