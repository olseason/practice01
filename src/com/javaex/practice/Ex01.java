package com.javaex.practice;

public class Ex01 {

	public static void main(String [] args) {
		
		/* int true;       -> true or false 중 나올 수 있는 자료형은 boolean형을 사용해야 한다.
		                   -> 자료형에 따른 저장하려는 공간의 이름과 =를 붙여줘야 한다.
		*/
		boolean n01 = true;
		
		
		
		/* int #_of_workers; -> int형은 정수형 자료형이기 때문에 사용할 수 없다.
		                     
		*/
		String n02 = "#_of_workers";
		
		
		
		/* int countOfLettersInString; -> int형은 정수형 자료형이기 때문에 사용할 수 없다.
		                               -> 또한 string은 ""을 사용한다. 
		*/
		String n03 = "countOfLettersInString";
		
		
		
		/* int 1stLevel1;           -> int형은 정수형 자료형이기 때문에 사용할 수 없다.
		*/
		String n04 = "1stLevel1";
		
		
		
		/* int person#;             -> int형은 정수형 자료형이기 때문에 사용할 수 없다.
	    */
		String n05 = "person#";
		
		
		
		
		/* int _person;             -> int형은 정수형 자료형이기 때문에 사용할 수 없다.
		                            
		*/
		String n06 = "_person";	
		
		
	}
}
