package com.javaex.practice;

public class Ex04 {

	public static void main(String[] args) {
		
		/*
		int x, y                    -> x, y의 값에 맞는 자료형을 써 주어야 한다. 
		
		x = 10                      -> x , y의 값이 정수이기 때문에 int 나 byte를 사용 
		y = 20                      -> 마무리한다는 ;도 표시 
		
		sum = x + y                 -> x + y의 값에 대한 자료형도 써 주어야 한다. 
		
		System.out.println("합은' + sum );  -> ""사용해야 함 
		*/
		
		
		int x = 10;
		int y = 20;
		
		int sum = x + y;
		
		System.out.println("합은" + sum );

	}

}
